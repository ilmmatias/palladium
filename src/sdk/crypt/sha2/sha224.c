#if __BYTE_ORDER__ == __ORDER_BIG_ENDIAN__
#define BSWAP(A) (A)
#else
#define BSWAP(A) BSWAP32((A))
#endif

#define ROTR(A, B) ROTR32((A), (B))

#define HIGH_BITS(A) (BSWAP((A) >> 29))
#define LOW_BITS(A) (BSWAP((A) << 3))

#define SUM0(A) (ROTR((A), 2) ^ ROTR((A), 13) ^ ROTR((A), 22))
#define SUM1(A) (ROTR((A), 6) ^ ROTR((A), 11) ^ ROTR((A), 25))

#define SIG0(A) (ROTR((A), 7) ^ ROTR((A), 18) ^ ((A) >> 3))
#define SIG1(A) (ROTR((A), 17) ^ ROTR((A), 19) ^ ((A) >> 10))

